#include <stdio.h>
int main(){      //Keyword ในบรรทัดนี้คือ int เเละ Identifier ในบรรทัดนี้คือ main 
    printf("Hello, C Token!\n"); //Identifier ในบรรทัดนี้คือ printf // constant ในบรรทัดนี้คือ "Hello, C Token!\n"
    return 0;    //Keyword ในบรรทัดนี้คือ return เเละ Constant ในบรรทัดนี้คือ 0
}                       //Operator ใน Code นี้คือ ()