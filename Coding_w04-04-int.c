#include <stdio.h>

int main() {
    int score = 90.5;  
    printf("score = %d\n", score);  
    return 0;
}

//ถ้าใช้ int กับค่าทศนิยม กับ score = 90.5; ผลลัพธ์จะกลายเป็น 90 เพราะ int ตัดทศนิยมทิ้ง
//ที่ต้องใช้ Data type ให้ถูกต้องเพราะถ้าใช้ผิด ค่าผลลัพธ์อาจจะเเสดงไม่ตรงตามที่กำหนด หรือไม่ถูกตามที่โจทย์ต้องการ 
//เช่น ถ้าใช้ int กับค่าทศนิยม ผลลัพธ์จะไม่เเสดงทศนิยมออกมาจึงต้องใช้ float
//หากอยากเเสดงเป็นจำนวนเต็มไม่มีทศนิยมก็ใช้ int ได้